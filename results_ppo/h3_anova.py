"""
h3_anova.py -- H3 Statistical Analysis
========================================
Runs one-way and two-way ANOVA on the H3 3x3 factorial sensitivity results
generated by Spataro_Final_TrainedV2.py.

Input:
    results_ppo/h3_results.csv       (generated by run_h3() in main script)

Outputs:
    results_ppo/h3_anova_report.txt  (full analysis report)

Run from anywhere -- the script locates results_ppo/ automatically:
    python h3_anova.py
    python results_ppo/h3_anova.py   (both work)

Attribution:
    Spataro, N.D. (2026). System of Systems Integration Framework for
    Next-Generation Air Traffic Management. D.Eng. Praxis, The George
    Washington University.
    AI tool used for code generation with author attribution per GWU policy.
"""

import os
import sys
import warnings
import numpy as np
import pandas as pd
from scipy import stats

warnings.filterwarnings('ignore')

# ── Path resolution (works from any working directory) ────────────────────────
SCRIPT_DIR  = os.path.dirname(os.path.abspath(__file__))
BASE_DIR    = (os.path.dirname(SCRIPT_DIR)
               if os.path.basename(SCRIPT_DIR) == 'results_ppo'
               else SCRIPT_DIR)

CSV_PATH    = os.path.join(BASE_DIR, 'results_ppo', 'h3_results.csv')
OUT_DIR     = os.path.join(BASE_DIR, 'results_ppo')
REPORT_PATH = os.path.join(OUT_DIR, 'h3_anova_report.txt')
ALPHA       = 0.05

DENSITY_ORDER = ['LOW', 'BASE', 'HIGH']
FREQ_ORDER    = ['FAST', 'MID',  'SLOW']


# =============================================================================
# DATA LOADING
# =============================================================================

def load_reductions(csv_path):
    """
    Load h3_results.csv and compute per-run risk score reductions.

    Pairs baseline and AI episodes by (density, freq, run) index,
    matching the paired structure used in the per-cell t-tests in run_h3().

    Reduction = (risk_base - risk_ai) / |risk_base| * 100
    Positive = AI outperforms baseline. Negative = AI worse than baseline.
    """
    if not os.path.exists(csv_path):
        sys.exit(
            '\nERROR: {} not found.\n'
            'Run Spataro_Final_TrainedV2.py first to generate H3 data.\n'
            .format(csv_path)
        )

    df = pd.read_csv(csv_path)

    required = {'density', 'freq', 'condition', 'run', 'risk_score'}
    missing  = required - set(df.columns)
    if missing:
        sys.exit('\nERROR: CSV missing columns: {}\n'.format(missing))

    base = (df[df['condition'] == 'baseline']
            [['density', 'freq', 'run', 'risk_score']]
            .rename(columns={'risk_score': 'risk_base'}))

    ai   = (df[df['condition'] == 'ai']
            [['density', 'freq', 'run', 'risk_score']]
            .rename(columns={'risk_score': 'risk_ai'}))

    merged = base.merge(ai, on=['density', 'freq', 'run'], how='inner')

    # Guard against zero baseline (avoids inf reduction values)
    safe_base = merged['risk_base'].replace(0, np.nan).abs()
    merged['reduction'] = (
        (merged['risk_base'] - merged['risk_ai']) / safe_base * 100
    )
    merged = merged.dropna(subset=['reduction'])

    merged['density'] = pd.Categorical(
        merged['density'], categories=DENSITY_ORDER, ordered=True
    )
    merged['freq'] = pd.Categorical(
        merged['freq'], categories=FREQ_ORDER, ordered=True
    )

    return merged.sort_values(['density', 'freq', 'run']).reset_index(drop=True)


# =============================================================================
# DESCRIPTIVE STATISTICS
# =============================================================================

def cell_summary(df):
    """
    Per-cell mean reduction, SD, SE, one-sample t-test vs zero,
    and Cohen's d. Reproduces Table 4-3 values.
    """
    rows = []
    for d in DENSITY_ORDER:
        for f in FREQ_ORDER:
            cell = df[(df['density'] == d) & (df['freq'] == f)]['reduction']
            n    = len(cell)
            if n < 3:
                continue
            mean  = cell.mean()
            sd    = cell.std(ddof=1)
            se    = sd / np.sqrt(n)
            t, p  = stats.ttest_1samp(cell, 0)
            d_eff = mean / sd if sd > 0 else 0.0
            rows.append({
                'Density'   : d,
                'Freq'      : f,
                'N'         : n,
                'Mean %'    : round(mean, 1),
                'SD'        : round(sd, 2),
                'SE'        : round(se, 2),
                't'         : round(t, 3),
                'p'         : round(p, 4),
                "Cohen's d" : round(d_eff, 2),
                'Sig'       : '* p<.05' if p < ALPHA else 'ns',
            })
    return pd.DataFrame(rows)


# =============================================================================
# ONE-WAY ANOVAs
# =============================================================================

def run_oneway_anovas(df):
    """
    One-way ANOVA for density main effect (collapsing across frequency)
    and frequency main effect (collapsing across density).
    """
    results = {}
    n_total = len(df)

    for factor, levels, col in [
        ('density',   DENSITY_ORDER, 'density'),
        ('frequency', FREQ_ORDER,    'freq'),
    ]:
        groups    = [df[df[col] == lvl]['reduction'].values for lvl in levels]
        F, p      = stats.f_oneway(*groups)
        df_btwn   = len(levels) - 1
        df_within = n_total - len(levels)

        results[factor] = {
            'F'          : round(F, 3),
            'p'          : round(p, 4),
            'df_between' : df_btwn,
            'df_within'  : df_within,
            'n'          : n_total,
            'sig'        : p < ALPHA,
        }

    return results


# =============================================================================
# TWO-WAY ANOVA
# =============================================================================

def run_twoway_anova(df):
    """
    Two-way ANOVA: reduction ~ C(density) + C(freq) + C(density):C(freq)
    Type II sums of squares via statsmodels OLS.

    For a balanced 3x3 design with n runs per cell (N = 9 * n):
        df(density)     = 2
        df(frequency)   = 2
        df(interaction) = 4
        df(residual)    = N - 9
    """
    try:
        import statsmodels.formula.api as smf
        from statsmodels.stats.anova import anova_lm
    except ImportError:
        sys.exit(
            '\nERROR: statsmodels required for two-way ANOVA.\n'
            'Install with: pip install statsmodels\n'
        )

    df2            = df.copy()
    df2['density'] = df2['density'].astype(str)
    df2['freq']    = df2['freq'].astype(str)

    model = smf.ols(
        'reduction ~ C(density) + C(freq) + C(density):C(freq)',
        data=df2
    ).fit()

    table = anova_lm(model, typ=2)
    return model, table


# =============================================================================
# TUKEY HSD POST-HOC
# =============================================================================

def run_tukey_hsd(df):
    """
    Tukey HSD on all nine density x frequency cell combinations.
    Only called when the interaction term is significant.
    """
    from statsmodels.stats.multicomp import pairwise_tukeyhsd

    df2         = df.copy()
    df2['cell'] = df2['density'].astype(str) + '_' + df2['freq'].astype(str)
    return pairwise_tukeyhsd(df2['reduction'], df2['cell'], alpha=ALPHA)


# =============================================================================
# SHAPIRO-WILK NORMALITY
# =============================================================================

def check_normality(df):
    """Shapiro-Wilk per cell. CLT supports ANOVA at n>=30 regardless."""
    rows = []
    for d in DENSITY_ORDER:
        for f in FREQ_ORDER:
            cell = df[(df['density'] == d) & (df['freq'] == f)]['reduction']
            if len(cell) >= 3:
                stat, p = stats.shapiro(cell)
                rows.append({
                    'Cell'           : '{}_{}'.format(d, f),
                    'N'              : len(cell),
                    'W'              : round(stat, 4),
                    'p'              : round(p, 4),
                    'Normal (p>.05)' : 'Yes' if p > 0.05 else 'No',
                })
    return pd.DataFrame(rows)


# =============================================================================
# HELPERS
# =============================================================================

def get_interaction_row(table):
    for idx in table.index:
        if ':' in str(idx):
            return table.loc[idx]
    return None


def get_residual_df(table):
    for idx in table.index:
        if 'Residual' in str(idx) or 'residual' in str(idx):
            return int(table.loc[idx, 'df'])
    return None


# =============================================================================
# REPORT WRITER
# =============================================================================

def write_report(df, summary, oneway, twoway_table, tukey, normality, path):
    """Write full analysis report to UTF-8 text file."""

    SEP  = '=' * 72
    SEP2 = '-' * 72

    inter  = get_interaction_row(twoway_table)
    df_res = get_residual_df(twoway_table)

    F_int  = float(inter['F'])      if inter is not None else 0.0
    p_int  = float(inter['PR(>F)']) if inter is not None else 1.0
    df_int = int(inter['df'])       if inter is not None else 0

    lines = [
        SEP,
        'H3 STATISTICAL ANALYSIS REPORT',
        'One-Way and Two-Way ANOVA -- Traffic Density x Update Frequency',
        '',
        'Spataro, N.D. (2026). System of Systems Integration Framework for',
        'Next-Generation Air Traffic Management. D.Eng. Praxis, GWU.',
        '',
        'Input  : {}'.format(CSV_PATH),
        'N total: {} paired observations ({} cells)'.format(
            len(df), df.groupby(['density', 'freq']).ngroups
        ),
        'Alpha  : {}'.format(ALPHA),
        SEP, '',
    ]

    # -- Normality --
    lines += [
        'SECTION 1: Shapiro-Wilk Normality Check (per cell)',
        SEP2,
        normality.to_string(index=False),
        '',
        'Note: CLT supports parametric ANOVA at n >= 30 per cell regardless',
        'of within-cell distribution shape.',
        '',
    ]

    # -- Cell summary --
    lines += [
        'SECTION 2: Per-Cell Descriptive Statistics (Table 4-3)',
        SEP2,
        summary.to_string(index=False),
        '',
        'One-sample t-test: tests whether mean reduction differs from zero.',
        'Positive Mean % = AI reduces risk vs baseline.',
        'Negative Mean % = AI increases risk vs baseline.',
        '',
    ]

    # -- One-way --
    lines += ['SECTION 3: One-Way ANOVA -- Main Effects', SEP2]
    for factor, res in oneway.items():
        sig = 'SIGNIFICANT **' if res['sig'] else 'Not significant'
        lines.append(
            '  {:<12}  F({},{}) = {:.3f}  p = {:.4f}  --> {}'.format(
                factor.upper(),
                res['df_between'], res['df_within'],
                res['F'], res['p'], sig
            )
        )
    lines += [
        '',
        'Interpretation: Determines whether density or frequency alone',
        'constitutes a statistically significant performance moderator.',
        '',
    ]

    # -- Two-way --
    lines += [
        'SECTION 4: Two-Way ANOVA -- Full Factorial (Type II SS)',
        SEP2,
        twoway_table.round(4).to_string(),
        '',
    ]
    if inter is not None:
        sig = 'SIGNIFICANT **' if p_int < ALPHA else 'Not significant'
        lines += [
            '  Interaction (density x freq):  '
            'F({},{}) = {:.3f}  p = {:.4f}  --> {}'.format(
                df_int, df_res, F_int, p_int, sig
            ),
            '',
            'Interpretation: Tests whether the joint density x frequency',
            'condition produces effects beyond what each factor contributes',
            'independently.',
            '',
        ]

    # -- Tukey HSD --
    lines += [
        'SECTION 5: Tukey HSD Post-Hoc -- Interaction Cell Pairs',
        SEP2,
    ]
    if isinstance(tukey, str):
        lines += [tukey, '']
    else:
        lines += [str(tukey.summary()), '']

    # -- Summary for paper --
    lines += [
        'SECTION 6: Summary Statistics for Paper',
        SEP2,
        '',
        'USE THESE VALUES to update Tables 4-3 and 4-4 and all H3 narrative:',
        '',
        'ONE-WAY ANOVA RESULTS (Table 4-4):',
    ]
    for factor, res in oneway.items():
        lines.append(
            '  {:<12}  F({},{}) = {:.3f}, p = {:.4f}  -- {}'.format(
                factor.capitalize(),
                res['df_between'], res['df_within'],
                res['F'], res['p'],
                'Significant' if res['sig'] else 'Not significant'
            )
        )
    lines += [
        '',
        'TWO-WAY ANOVA INTERACTION:',
    ]
    if inter is not None:
        lines.append(
            '  F({},{}) = {:.3f}, p = {:.4f}  -- {}'.format(
                df_int, df_res, F_int, p_int,
                'Significant' if p_int < ALPHA else 'Not significant'
            )
        )
    lines += [
        '',
        'CELL MEANS FOR TABLE 4-3:',
    ]
    for _, row in summary.iterrows():
        lines.append(
            '  {}_{:<6}  N={:2d}  Mean={:7.1f}%  p={:.4f}  {}'.format(
                row['Density'], row['Freq'],
                int(row['N']), float(row['Mean %']),
                float(row['p']), row['Sig']
            )
        )
    lines += ['', SEP]

    os.makedirs(OUT_DIR, exist_ok=True)
    with open(path, 'w', encoding='utf-8') as fh:   # utf-8 fixes Windows cp1252 error
        fh.write('\n'.join(lines) + '\n')
    print('\nReport saved --> {}'.format(path))


# =============================================================================
# MAIN
# =============================================================================

def main():
    print('\nH3 ANOVA Analysis -- Spataro (2026) D.Eng. Praxis, GWU')
    print('=' * 60)

    # Load
    print('\n[1/6] Loading {} ...'.format(CSV_PATH))
    df = load_reductions(CSV_PATH)
    runs = df.groupby(['density', 'freq']).size()
    print('      {} paired observations | {} cells | {}-{} runs/cell'.format(
        len(df), len(runs), runs.min(), runs.max()
    ))
    if runs.min() < 30:
        print('      NOTE: Some cells have fewer than 30 runs '
              '(episodes terminated early). This is normal.')

    # Normality
    print('\n[2/6] Shapiro-Wilk normality check ...')
    normality = check_normality(df)
    n_normal  = (normality['Normal (p>.05)'] == 'Yes').sum()
    print('      {}/{} cells normally distributed'.format(n_normal, len(normality)))
    print('      (CLT supports ANOVA at n>=30 regardless)')

    # Cell summary
    print('\n[3/6] Cell descriptive statistics ...')
    summary = cell_summary(df)
    print(summary[['Density', 'Freq', 'N', 'Mean %', 'p', 'Sig']].to_string(index=False))

    # One-way ANOVAs
    print('\n[4/6] One-way ANOVAs ...')
    oneway = run_oneway_anovas(df)
    for factor, res in oneway.items():
        sig = 'SIGNIFICANT **' if res['sig'] else 'Not significant'
        print('      {:<12}  F({},{}) = {:.3f}  p = {:.4f}  {}'.format(
            factor, res['df_between'], res['df_within'],
            res['F'], res['p'], sig
        ))

    # Two-way ANOVA
    print('\n[5/6] Two-way ANOVA (density x frequency) ...')
    model, twoway_table = run_twoway_anova(df)
    inter  = get_interaction_row(twoway_table)
    df_res = get_residual_df(twoway_table)
    if inter is not None:
        F_int  = float(inter['F'])
        p_int  = float(inter['PR(>F)'])
        df_int = int(inter['df'])
        sig    = 'SIGNIFICANT **' if p_int < ALPHA else 'Not significant'
        print('      Interaction: F({},{}) = {:.3f}  p = {:.4f}  {}'.format(
            df_int, df_res, F_int, p_int, sig
        ))

    # Tukey HSD
    print('\n[6/6] Tukey HSD post-hoc ...')
    if inter is not None and float(inter['PR(>F)']) < ALPHA:
        tukey    = run_tukey_hsd(df)
        sig_pairs = sum(1 for r in tukey.summary().data[1:] if r[-1])
        print('      {} significant pairwise differences found'.format(sig_pairs))
    else:
        tukey = 'Tukey HSD not run: interaction not significant (p >= {})'.format(ALPHA)
        print('      Skipped -- interaction not significant')

    # Write report
    write_report(df, summary, oneway, twoway_table, tukey, normality, REPORT_PATH)

    # Print Section 6 summary to console too
    print('\n' + '=' * 60)
    print('SUMMARY FOR PAPER')
    print('=' * 60)
    print('\nOne-way ANOVA results (Table 4-4):')
    for factor, res in oneway.items():
        print('  {}: F({},{}) = {:.3f}, p = {:.4f}  -- {}'.format(
            factor.capitalize(),
            res['df_between'], res['df_within'],
            res['F'], res['p'],
            'Significant' if res['sig'] else 'Not significant'
        ))
    if inter is not None:
        F_int  = float(inter['F'])
        p_int  = float(inter['PR(>F)'])
        df_int = int(inter['df'])
        print('\nTwo-way ANOVA interaction:')
        print('  F({},{}) = {:.3f}, p = {:.4f}  -- {}'.format(
            df_int, df_res, F_int, p_int,
            'Significant' if p_int < ALPHA else 'Not significant'
        ))
    print('\nCell means (for Table 4-3):')
    for _, row in summary.iterrows():
        print('  {}_{:<6}  N={:2d}  {:.1f}%  p={:.4f}  {}'.format(
            row['Density'], row['Freq'],
            int(row['N']), float(row['Mean %']),
            float(row['p']), row['Sig']
        ))
    print('\nDone.')


if __name__ == '__main__':
    main()